@Injectable()
export class PasswordDenylistService {
  private sha1(input: string): string {
    return crypto.createHash('sha1').update(input).digest('hex').toUpperCase();
  }

  isBreached(password: string): boolean {
    const hash = this.sha1(password);
    return BREACHED_SHA1_SET.has(hash);
  }

  validatePasswordPolicy(
    password: string,
    email: string,
    displayName: string,
  ): { valid: boolean; reason?: string } {
    if (password.length < PASSWORD_MIN_LENGTH) {
      return { valid: false, reason: `Password must be at least ${PASSWORD_MIN_LENGTH} characters` };
    }

    if (!/[a-zA-Z]/.test(password)) {
      return { valid: false, reason: 'Password must contain at least one letter' };
    }

    if (!/[0-9]/.test(password)) {
      return { valid: false, reason: 'Password must contain at least one number' };
    }

    const lowerPass = password.toLowerCase();

    const emailLocal = email.split('@')[0]!.toLowerCase();
    if (emailLocal.length >= 4 && lowerPass.includes(emailLocal)) {
      return { valid: false, reason: 'Password must not contain your email address' };
    }

    const emailDomain = email.split('@')[1]?.split('.')[0]?.toLowerCase();
    if (emailDomain && emailDomain.length >= 4 && lowerPass.includes(emailDomain)) {
      return { valid: false, reason: 'Password must not contain your email domain' };
    }

    const displayFragments = displayName
      .toLowerCase()
      .split(/[\s.\-_]+/)
      .filter((f) => f.length >= 4);
    for (const fragment of displayFragments) {
      if (lowerPass.includes(fragment)) {
        return { valid: false, reason: 'Password must not contain parts of your display name' };
      }
    }

    if (this.isBreached(password)) {
      return { valid: false, reason: 'This password has appeared in a data breach and cannot be used' };
    }

    return { valid: true };
  }
}
